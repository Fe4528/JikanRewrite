class TempTime {
    constructor() {
        this.servers = new Map()
    }

    /**
     * Add a server to the servers Map
     * @param {string} server_id
     */
    addServer(server_id) {
        this.servers.set()
    }

    /**
     * Get the temp time of a server
     * @param {string} server_id 
     * @returns {Map<string, Map>}
     */
    getServer(server_id) {
        return this.servers.get()
    }
}